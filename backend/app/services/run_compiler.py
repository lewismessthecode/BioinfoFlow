"""RunCompiler — the single code path from RunCreate envelope to a
persisted, queued Run.

Replaces the legacy ``RunSubmissionService.create_run_submission`` /
``create_run_unified`` (928 lines of hint-driven compilation) with a
direct, form_spec-driven compile:

    payload (RunCreate)
        → validate against Workflow.form_spec
        → resolve asset:// URIs to absolute paths
        → materialize table values as CSV, JSON inputs as .json
        → build adapter-ready config dict
        → construct the engine's launch spec (argv + env + cwd)
        → create Run row, persist audit artifacts, queue + dispatch

The compile step owns every file write that must exist before the
scheduler dispatches the run. Adapter contract (``build_command``,
``parse_event``, ``post_complete``, ``cancel``) is unchanged; we just
drive it at compile time so ``audit/launch.sh`` is a real, replayable
snapshot of what the scheduler will execute.
"""

